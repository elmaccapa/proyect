<h1><u>	HISTORIA</u></h1>
<p> <b>La cocina peruana es considerada una de las más variadas del mundo. Gracias a la herencia pre incaica, incaica y española con 774 años de convivencia musulmana, y a la inmigración africana, francesa, chino-cantonesa, japonesa e italiana principalmente hasta el siglo XIX, reúne una gran diversidad de mezclas, junto con la criolla, en una gastronomia de cuatro continentes en tan solo un país, ofreciendo una importante variedad de platos típicos de arte culinario peruano en constante evolución, imposible de enumerarlos en su totalidad. Solo en la costa peruana, hay más de dos mil sopas diferentes y que en el país hay más de 250 postres tradicionales.</b> </p>
 
<p><b>Hoy en día cuando un visitante o incluso un peruano se sientan a degustar alguno de los platos peruanos, quizá no sabe que está experimentando el resultado de una fascinante evolución de comidas y culturas. Con la llegada de los españoles se introdujo nuevas especies de animales, frutas y plantas. Esta fusión de la comida inca con la española dio a luz a la comida criolla apareciendo nuevas recetas como la Ocopa o la quinua que era considerada una comida sagrada por los incas.
Pero la historia continua, la llegada de los esclavos africanos que cocinaban en las cocinas del Virreinato, aporto con más de un granito de arena a la evolución de nuestra cocina aportando sabor y color, es gracias a ellos que nace el delicioso anticucho de corazón. </b></p>
 
<p><b>En el siglo XIX la revolución peruana se avivo. La presencia y aporte francés a la comida peruana es el resultado de la fascinación que tenían los Libertadores a todos los productos franceses. Después de la independencia, San Martin decreto la entrada libre a los extranjeros. En 1857 había un estimado de 20,000 europeos viviendo en Lima, estos incluían franceses, escoceses, ingleses, alemanes e italianos, así como también ciudadanos de la mayoría de países escandinavos y el resto de la cuenca del mediterráneo. Por otra parte en 1899, llegaron los primeros inmigrantes japoneses, dejando su huella distintiva y esencial en todas las comidas del Perú.
Como hemos observado la cocina peruana representa una simbiosis de culturas culinarias. No solo tiene hondas raíces preincas, incas y coloniales, sino también que a lo largo de su historia ha incorporado estilos gastronómicos mulatos, europeos y orientales. </b></P>
