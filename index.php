<h2><u>ÉPOCA PRECOLOMBIANA</u></h2>

<P><b>Los andes centrales peruanos fueron el más grande centro de domesticación de plantas del mundo antiguo, con especies nativas como el maíz, tubérculos con dos mil quinientas variedades de papa, muchas de camote, yuca o mandioca, oca, maca; gramíneas quinua, kiwicha o amaranto, cañihua; frutas como chirimoya, lúcuma, pacae, tomate, calabaza, palta, tumbo, sauco; leguminosas tales como frijoles, pallares, maní y una infinidad de hierbas aromáticas. </b></P>

<P><b>Antes del arribo europeo, la geografía peruana albergaba una gran variedad de culturas, conquistadas todas por el Imperio incaico, cada una de las cuales tenía características gastronómicas particulares, aunque había algunas generalidades, de acuerdo con los cronistas de la conquista. Por ejemplo, los principales condimentos eran hierbas aromáticas, cocha yuyo (un tipo de alga fluvial), sal y, sobre todo, el ají, llamado uchu en tiempos incas y considerado hoy un elemento fundamental de la cocina peruana.</b></P>

