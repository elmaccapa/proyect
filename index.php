<H4><u>ÉPOCA REPUBLICANA</u></H4>
<P><b>	Luego de la independencia se dieron una serie de migraciones de diversas procedencias que integraron sus propias tradiciones a la ya dinámica culinaria local. La migración de los chinos-cantoneses de mediados del siglo XIX popularizó el salteado a fuego fuerte y los sabores agridulces en las carnes además del uso de nuevas hierbas y de la salsa de soya (sillao). Pero su aporte más notorio fue el arroz. Si bien ya se consumía desde el siglo XVI, es luego de la migración china que el arroz se popularizó y se convirtió en la guarnición peruana por excelencia, en detrimento del pan.</b></P>

<P><b>La forma de arroz favorita en el Perú es el arroz graneado no demasiado cocido, se hace con arroz de grano largo, sin embargo, se distanciaba de la preparación china en el uso del ajo y la sal. Otra inmigración en la segunda mitad del siglo XIX no menos influyente es la italiana, que popularizó el uso de las pastas, el pastel de acelga, los dulces y postres como el panetón, que es obligado en las navidades a lo largo del país.</b></P>

