<H3><u>ÉPOCA COLONIAL</u></H3>
<P>
<b>Desde el inicio de la presencia española, se incorporaron nuevos usos y costumbres culinarios con el comienzo del virreinato. La fritura, el uso de los lácteos (incorporado a algunos chupes o sopas), además de la carne de res, cerdo, huevo de gallina y nuevas aves de corral; además llegaron algunos cultivos que resultarían esenciales para la nueva cocina como la cebolla y el ajo que combinados con el ají serían los principales ingredientes de muchos platos peruanos.

La lima traída por los españoles y adaptado con el tiempo a la tierra peruana, se fue transformando en la variedad peruana actual denominada limón, de color verde, pequeño y ácido y que deviene en uno de los componentes básicos del cebiche. La vid (de la que se origina el pisco) y los vinos llegan también al comienzo de este período. En los primeros encuentros entre españoles y nativos, durante la conquista del imperio incaico, intercambiaron los trozos de cerdo ibérico frito con papas, camotes y maíz autóctono. </b></P>

